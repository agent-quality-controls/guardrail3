#[test]
fn older_member_edition_warns() {
    let workspace_manifest = format!(
        r#"
            [workspace]
            members = ["crates/legacy"]
            resolver = "2"

            [workspace.package]
            edition = "2024"
            rust-version = "1.85"

            {workspace_rust_lints}
            {workspace_clippy_lints}
        "#
    );
    let results = check_results(&tree(
        &[
            ("", entry(&["crates"], &["Cargo.toml"])),
            ("crates", entry(&["legacy"], &[])),
            ("crates/legacy", entry(&[], &["Cargo.toml"])),
        ],
        &[
            ("Cargo.toml", &workspace_manifest),
            (
                "crates/legacy/Cargo.toml",
                r#"
                    [package]
                    name = "legacy"
                    edition = "2021"

                    [lints]
                    workspace = true
                "#,
            ),
        ],
    ));

    guardrail3_app_rs_family_cargo_assertions::rs_cargo_09_member_edition_drift::assert_rule_results(
        &results,
        &[ExpectedRuleResult {
            file: None,
            title: Some("member edition older than workspace"),
            inventory: None,
        }],
    );
}

