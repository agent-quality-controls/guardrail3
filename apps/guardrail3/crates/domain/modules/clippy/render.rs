use std::fmt::Write;

use crate::domain::modules::Module;

use super::macros::MACRO_DEBUGGING;
use super::methods::{
    METHOD_GARDE_DESERIALIZATION, library_profile_methods, service_profile_methods,
};
use super::settings::SETTINGS;
use super::thresholds::THRESHOLDS;
use super::types::{
    TYPE_GARDE_EXTRACTORS, library_profile_types, pure_layer_extra_types, service_profile_types,
};

/// Build the full clippy.toml content for a workspace root or crate.
///
/// `is_pure_layer` adds global-state type bans (for service profiles with pure-layer crates).
/// `extra_methods` and `extra_types` are appended from local override files.
pub fn build_clippy_toml(
    profile: &str,
    is_pure_layer: bool,
    garde_enabled: bool,
    extra_methods: &str,
    extra_types: &str,
) -> String {
    let mut methods = match profile {
        "library" => library_profile_methods(),
        _ => service_profile_methods(),
    };
    if !garde_enabled {
        methods.retain(|module| module.name != METHOD_GARDE_DESERIALIZATION.name);
    }

    let mut types = match profile {
        "library" => library_profile_types(),
        _ => {
            let mut profile_types = service_profile_types();
            if is_pure_layer {
                profile_types.extend(pure_layer_extra_types());
            }
            profile_types
        }
    };
    if !garde_enabled {
        types.retain(|module| module.name != TYPE_GARDE_EXTRACTORS.name);
    }

    let mut out = String::new();
    out.push_str(
        "# =============================================================================\n",
    );
    let _ = writeln!(
        out,
        "# clippy.toml -- GENERATED by guardrail3 (profile: {profile})"
    );
    out.push_str("# DO NOT EDIT -- regenerate with: guardrail3 generate\n");
    out.push_str(
        "# =============================================================================\n\n",
    );

    out.push_str("# THRESHOLDS\n");
    out.push_str(THRESHOLDS);
    out.push_str("\n\n");
    out.push_str("# SETTINGS\n");
    out.push_str(SETTINGS);
    out.push_str("\n\n");

    render_module_array(
        &mut out,
        "DISALLOWED METHODS",
        "disallowed-methods",
        &methods,
        extra_methods,
    );
    out.push('\n');
    render_module_array(
        &mut out,
        "DISALLOWED TYPES",
        "disallowed-types",
        &types,
        extra_types,
    );
    out.push('\n');
    render_module_array(
        &mut out,
        "DISALLOWED MACROS",
        "disallowed-macros",
        &[&MACRO_DEBUGGING],
        "",
    );

    out
}

fn render_module_array(
    out: &mut String,
    header: &str,
    key: &str,
    modules: &[&Module],
    extra_entries: &str,
) {
    out.push_str("# ");
    out.push_str(header);
    out.push('\n');
    out.push_str(key);
    out.push_str(" = [\n");
    for module in modules {
        let _ = writeln!(out, "    # --- {} ---", module.description);
        out.push_str(module.content);
        out.push('\n');
    }
    let deduped =
        crate::adapters::inbound::cli::generate::deduplicated_override(out, extra_entries);
    if !deduped.trim().is_empty() {
        out.push_str("    # --- Local overrides ---\n");
        out.push_str(&deduped);
    }
    out.push_str("]\n");
}
