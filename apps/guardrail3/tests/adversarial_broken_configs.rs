// ---------------------------------------------------------------------------
// Test 9: ts init --force removes old [typescript] subsections, preserves [rust]
// ---------------------------------------------------------------------------

#[test]
#[allow(clippy::expect_used)] // reason: test -- panics indicate broken test infrastructure
#[allow(clippy::disallowed_methods)] // reason: test uses Command and fs
fn ts_init_force_removes_old_subsections() {
    let tmp = tempfile::tempdir().expect("failed to create temp dir");
    let dir = tmp.path();

    // Write guardrail3.toml with existing [typescript] and [rust] sections
    std::fs::write(
        dir.join("guardrail3.toml"),
        concat!(
            "version = \"0.1\"\n",
            "\n",
            "[profile]\n",
            "name = \"service\"\n",
            "\n",
            "[typescript]\n",
            "\n",
            "[typescript.apps.old-app]\n",
            "type = \"service\"\n",
            "\n",
            "[typescript.apps.old-app.checks]\n",
            "architecture = true\n",
            "content = false\n",
            "tests = true\n",
            "\n",
            "[rust]\n",
            "workspace_root = \".\"\n",
        ),
    )
    .expect("write guardrail3.toml with old typescript");

    // Create apps/new-app/package.json so ts init discovers it
    let new_app_dir = dir.join("apps/new-app");
    std::fs::create_dir_all(&new_app_dir).expect("create apps/new-app");
    std::fs::write(
        new_app_dir.join("package.json"),
        r#"{"name": "new-app", "version": "1.0.0"}"#,
    )
    .expect("write package.json");

    let path_str = dir.to_str().expect("non-utf8 path");
    let out = guardrail3()
        .args(["ts", "init", "--force", path_str])
        .output()
        .expect("failed to run ts init");

    assert!(
        out.status.success(),
        "ts init --force should succeed: {}",
        String::from_utf8_lossy(&out.stderr)
    );

    // Read resulting guardrail3.toml
    let config =
        std::fs::read_to_string(dir.join("guardrail3.toml")).expect("read updated guardrail3.toml");

    // [rust] section should be preserved
    assert!(
        config.contains("[rust]"),
        "[rust] section should be preserved, got:\n{config}"
    );
    assert!(
        config.contains("workspace_root"),
        "workspace_root should be preserved, got:\n{config}"
    );

    // old-app should NOT appear
    assert!(
        !config.contains("old-app"),
        "old-app should be removed by --force, got:\n{config}"
    );

    // new-app should appear in the new typescript section
    assert!(
        config.contains("new-app"),
        "new-app should be discovered and added, got:\n{config}"
    );
}
