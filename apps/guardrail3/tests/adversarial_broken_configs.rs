/// Set up the broken configs fixture directory.
#[allow(clippy::disallowed_methods)] // reason: test helper -- writes fixture files to temp dir
#[allow(clippy::expect_used)] // reason: test helper -- panics on write failure
fn setup_broken_configs(dir: &std::path::Path) {
    // Cargo.toml: workspace with a single crate
    std::fs::write(
        dir.join("Cargo.toml"),
        concat!(
            "[workspace]\n",
            "members = [\"crates/*\"]\n",
            "resolver = \"2\"\n",
        ),
    )
    .expect("write Cargo.toml");

    // crates/app/Cargo.toml
    std::fs::create_dir_all(dir.join("crates/app")).expect("create crates/app");
    std::fs::write(
        dir.join("crates/app/Cargo.toml"),
        "[package]\nname = \"broken-app\"\nversion = \"0.1.0\"\nedition = \"2024\"\n",
    )
    .expect("write crates/app/Cargo.toml");

    // guardrail3.toml with unknown type "frontend"
    std::fs::write(
        dir.join("guardrail3.toml"),
        concat!(
            "version = \"0.1\"\n",
            "\n",
            "[profile]\n",
            "name = \"service\"\n",
            "\n",
            "[rust]\n",
            "workspace_root = \".\"\n",
            "\n",
            "[rust.apps.app]\n",
            "type = \"frontend\"\n",
        ),
    )
    .expect("write guardrail3.toml");

    // clippy.toml with CRLF line endings
    let crlf_content = CANONICAL_RUSTFMT.replace('\n', "\r\n");
    // Use this as a pre-existing clippy.toml to test CRLF detection
    // (we use rustfmt content as a stand-in -- it's the format difference that matters)
    std::fs::write(dir.join("clippy.toml"), crlf_content).expect("write CRLF clippy.toml");

    // deny.toml with UTF-8 BOM
    let bom_deny = format!(
        "\u{FEFF}{}",
        concat!(
            "[graph]\n",
            "all-features = true\n",
            "\n",
            "[bans]\n",
            "multiple-versions = \"warn\"\n",
            "deny = [\n",
            "    { name = \"openssl\", wrappers = [] },\n",
            "]\n",
        )
    );
    std::fs::write(dir.join("deny.toml"), bom_deny).expect("write BOM deny.toml");

    // rustfmt.toml: empty file (0 bytes)
    std::fs::write(dir.join("rustfmt.toml"), "").expect("write empty rustfmt.toml");

    // .guardrail3/overrides/ with various broken files
    let overrides_dir = dir.join(".guardrail3/overrides");
    std::fs::create_dir_all(&overrides_dir).expect("create overrides dir");

    // clippy-methods.toml with BOM + valid entry
    std::fs::write(
        overrides_dir.join("clippy-methods.toml"),
        "\u{FEFF}    { path = \"std::process::Command::new\", reason = \"Banned\" },\n",
    )
    .expect("write BOM clippy-methods.toml");

    // deny-bans.toml with mix: valid, invalid, null byte
    std::fs::write(
        overrides_dir.join("deny-bans.toml"),
        "    { name = \"custom-ban\", wrappers = [] },\nthis is not valid toml\n    { name = \"another-ban\0\", wrappers = [] },\n",
    )
    .expect("write mixed deny-bans.toml");

    // mystery-file.toml: unrecognized override name
    std::fs::write(
        overrides_dir.join("mystery-file.toml"),
        "    { path = \"mystery::function\", reason = \"should not appear\" },\n",
    )
    .expect("write mystery-file.toml");
}

// ---------------------------------------------------------------------------
// Test 1: unknown type "frontend" falls back to service profile
// ---------------------------------------------------------------------------

#[test]
#[allow(clippy::expect_used)] // reason: test -- panics indicate broken test infrastructure
#[allow(clippy::disallowed_methods)] // reason: test uses Command and fs
fn unknown_type_falls_back_to_service() {
    let tmp = tempfile::tempdir().expect("failed to create temp dir");
    let dir = tmp.path();
    setup_broken_configs(dir);

    let path_str = dir.to_str().expect("non-utf8 path");
    let out = guardrail3()
        .args(["rs", "generate", "--dry-run", path_str])
        .output()
        .expect("failed to run");

    // Should not crash
    let stderr = String::from_utf8_lossy(&out.stderr);
    let stdout = String::from_utf8_lossy(&out.stdout);

    // Should show clippy.toml in output (service profile used as fallback)
    assert!(
        stdout.contains("clippy.toml"),
        "Diff output should mention clippy.toml (service fallback), got stdout:\n{stdout}\nstderr:\n{stderr}"
    );
}

